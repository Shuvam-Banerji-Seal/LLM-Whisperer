"""Core RAG evaluation implementations."""

import logging
from typing import List, Dict, Any, Optional, Tuple
from abc import ABC, abstractmethod
from dataclasses import dataclass

from .config import EvalConfig, EvalMetric

logger = logging.getLogger(__name__)


@dataclass
class EvaluationResult:
    """Result of RAG evaluation."""

    metric_name: str
    score: float
    metadata: Dict[str, Any]


class RAGEvaluator:
    """Main evaluator for RAG systems.

    Evaluates retrieval quality, generation quality, and faithfulness.
    """

    def __init__(self, config: EvalConfig):
        """Initialize RAG evaluator.

        Args:
            config: Evaluation configuration
        """
        self.config = config
        self.metric_calculator = MetricCalculator()
        self.results: List[EvaluationResult] = []

    def evaluate_retrieval(
        self,
        retrieved_docs: List[str],
        relevant_docs: List[str],
        query: str,
    ) -> Dict[str, float]:
        """Evaluate retrieval quality.

        Args:
            retrieved_docs: Documents retrieved by system
            relevant_docs: Ground truth relevant documents
            query: Original query

        Returns:
            Dictionary of retrieval metrics
        """
        metrics = {}

        if EvalMetric.RETRIEVAL_NDCG in self.config.metrics:
            metrics["ndcg"] = self.metric_calculator.calculate_ndcg(
                retrieved_docs, relevant_docs, self.config.top_k
            )

        if EvalMetric.RETRIEVAL_RECALL in self.config.metrics:
            metrics["recall"] = self.metric_calculator.calculate_recall(
                retrieved_docs, relevant_docs, self.config.top_k
            )

        logger.info(f"Retrieval evaluation: {metrics}")
        return metrics

    def evaluate_generation(
        self,
        generated_text: str,
        reference_text: str,
    ) -> Dict[str, float]:
        """Evaluate generation quality.

        Args:
            generated_text: Text generated by system
            reference_text: Reference/ground truth text

        Returns:
            Dictionary of generation metrics
        """
        metrics = {}

        if EvalMetric.GENERATION_ROUGE in self.config.metrics:
            metrics["rouge"] = self.metric_calculator.calculate_rouge(
                generated_text, reference_text
            )

        logger.info(f"Generation evaluation: {metrics}")
        return metrics

    def evaluate_faithfulness(
        self,
        generated_text: str,
        context_docs: List[str],
    ) -> Dict[str, float]:
        """Evaluate faithfulness of generation to context.

        Args:
            generated_text: Generated text to evaluate
            context_docs: Context documents used for generation

        Returns:
            Dictionary with faithfulness metrics
        """
        context = " ".join(context_docs)

        faithfulness_score = self.metric_calculator.calculate_faithfulness(
            generated_text, context
        )

        return {"faithfulness": faithfulness_score}


class MetricCalculator:
    """Calculates RAG evaluation metrics."""

    def calculate_ndcg(
        self, retrieved: List[str], relevant: List[str], k: int
    ) -> float:
        """Calculate NDCG (Normalized Discounted Cumulative Gain).

        Args:
            retrieved: Retrieved documents
            relevant: Relevant documents
            k: Top-k cutoff

        Returns:
            NDCG score between 0 and 1
        """
        dcg = 0.0
        for i, doc in enumerate(retrieved[:k]):
            if doc in relevant:
                dcg += 1.0 / (i + 2)  # log2(i+2)

        idcg = 0.0
        for i in range(min(k, len(relevant))):
            idcg += 1.0 / (i + 2)

        return dcg / idcg if idcg > 0 else 0.0

    def calculate_recall(
        self, retrieved: List[str], relevant: List[str], k: int
    ) -> float:
        """Calculate Recall@k.

        Args:
            retrieved: Retrieved documents
            relevant: Relevant documents
            k: Top-k cutoff

        Returns:
            Recall score between 0 and 1
        """
        if not relevant:
            return 1.0

        hits = sum(1 for doc in retrieved[:k] if doc in relevant)
        return hits / len(relevant)

    def calculate_rouge(self, generated: str, reference: str) -> float:
        """Calculate ROUGE-L score.

        Args:
            generated: Generated text
            reference: Reference text

        Returns:
            ROUGE-L F1 score
        """
        # Simplified ROUGE-L calculation
        gen_tokens = set(generated.lower().split())
        ref_tokens = set(reference.lower().split())

        if not ref_tokens:
            return 1.0 if not gen_tokens else 0.0

        overlap = len(gen_tokens & ref_tokens)
        precision = overlap / len(gen_tokens) if gen_tokens else 0.0
        recall = overlap / len(ref_tokens)

        if precision + recall == 0:
            return 0.0

        f1 = 2 * (precision * recall) / (precision + recall)
        return f1

    def calculate_faithfulness(self, generated: str, context: str) -> float:
        """Calculate faithfulness score.

        Args:
            generated: Generated text
            context: Context from which text was generated

        Returns:
            Faithfulness score between 0 and 1
        """
        # Simplified: measure token overlap
        gen_tokens = set(generated.lower().split())
        ctx_tokens = set(context.lower().split())

        if not gen_tokens:
            return 1.0

        faithful_tokens = sum(1 for token in gen_tokens if token in ctx_tokens)
        return faithful_tokens / len(gen_tokens)


class BenchmarkRunner:
    """Runs benchmarks on RAG systems."""

    def __init__(self, evaluator: RAGEvaluator):
        """Initialize benchmark runner.

        Args:
            evaluator: RAGEvaluator instance
        """
        self.evaluator = evaluator
        self.results: Dict[str, Any] = {}

    def run_benchmark(
        self,
        queries: List[str],
        retrieved_results: List[List[str]],
        relevant_results: List[List[str]],
        generated_texts: List[str],
        reference_texts: List[str],
    ) -> Dict[str, float]:
        """Run complete benchmark.

        Args:
            queries: List of queries
            retrieved_results: Retrieved documents for each query
            relevant_results: Ground truth relevant documents
            generated_texts: Generated texts
            reference_texts: Reference texts

        Returns:
            Dictionary of benchmark results
        """
        all_metrics = {}

        for i, query in enumerate(queries):
            retrieval_metrics = self.evaluator.evaluate_retrieval(
                retrieved_results[i], relevant_results[i], query
            )
            all_metrics[f"query_{i}_retrieval"] = retrieval_metrics

            if i < len(generated_texts):
                gen_metrics = self.evaluator.evaluate_generation(
                    generated_texts[i], reference_texts[i]
                )
                all_metrics[f"query_{i}_generation"] = gen_metrics

        logger.info(f"Benchmark complete with {len(queries)} queries")
        self.results = all_metrics
        return all_metrics
